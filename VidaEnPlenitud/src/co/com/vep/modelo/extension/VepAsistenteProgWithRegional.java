package co.com.vep.modelo.extension;

import co.com.vep.modelo.VepAsistenteProg;

public class VepAsistenteProgWithRegional {
	
	private VepAsistenteProg asistente;
	private String regional;
	/**
	 * @return the asistente
	 */
	public VepAsistenteProg getAsistente() {
		return asistente;
	}
	/**
	 * @param asistente the asistente to set
	 */
	public void setAsistente(VepAsistenteProg asistente) {
		this.asistente = asistente;
	}
	/**
	 * @return the regional
	 */
	public String getRegional() {
		return regional;
	}
	/**
	 * @param regional the regional to set
	 */
	public void setRegional(String regional) {
		this.regional = regional;
	}

}
